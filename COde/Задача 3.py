def manage_grades(student_name: str, subject: str):
    # Предзаданная база данных
    grades_db = {
        'Иванов': {
            'Математика': [5, 4, 5, 3, 4],
            'Русский язык': [4, 4, 5, 5, 3],
            'Физика': [5, 5, 4, 4, 5],
            'История': [3, 4, 4, 4, 5],
        },
        'Петров': {
            'Математика': [4, 4, 3, 5, 4],
            'Русский язык': [5, 5, 4, 4, 5],
            'Физика': [3, 4, 4, 3, 4],
            'История': [5, 5, 5, 4, 5],
        },
        'Сидоров': {
            'Математика': [3, 3, 4, 4, 3],
            'Русский язык': [4, 3, 5, 4, 4],
            'Физика': [5, 4, 5, 5, 5],
            'История': [4, 4, 3, 4, 4],
        }
    }

    # Проверки, что ученик и предмет есть
    if student_name not in grades_db:
        print(f"Ученик с фамилией '{student_name}' не найден.")
        return
    if subject not in grades_db[student_name]:
        print(f"Предмет '{subject}' не найден для ученика {student_name}.")
        return

    # Список оценок
    marks = grades_db[student_name][subject]

    print("Выберите действие:")
    print("1 — показать все оценки")
    print("2 — показать средний балл")

    choice = input("Введите 1 или 2: ").strip()
    if choice == '1':
        # Вывод всех оценок
        print(f"Оценки ученика {student_name} по предмету {subject}: {marks}")
    elif choice == '2':
        # Вычисление среднего
        if len(marks) > 0:
            avg = sum(marks) / len(marks)
            print(f"Средний балл ученика {student_name} по предмету {subject}: {avg:.2f}")
        else:
            print("Нет оценок, чтобы рассчитать средний балл.")
    else:
        print("Неверный выбор. Пожалуйста, введите 1 или 2.")


# Пример вызовов
if __name__ == "__main__":
    # Можно попросить пользователя ввести данные
    s = input("Введите фамилию ученика: ").strip()
    subj = input("Введите название предмета: ").strip()
    manage_grades(s, subj)
